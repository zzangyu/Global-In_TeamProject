package com.city.model;

public class FreeEvaluationVO {
	private int num;
	private String userID;
	private int gechu;
	private int bichu;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getGechu() {
		return gechu;
	}
	public void setGechu(int gechu) {
		this.gechu = gechu;
	}
	public int getBichu() {
		return bichu;
	}
	public void setBichu(int bichu) {
		this.bichu = bichu;
	}
}
